-- ====================
-- 3. REGION TABLE
-- ====================
-- Represents provinces, states, or regions linked to a country.
-- RegionID is formed using the format 'CountryCode-RegionCode' (e.g., 'ZAF-GAU').

CREATE TABLE address.region (
    RegionID CHAR(7) PRIMARY KEY,             -- Composite format: CountryCode + '-' + Abbreviation (e.g., 'ZAF-GAU')
    RegionName NVARCHAR(100) NOT NULL,        -- Name of the region or province
    CountryID CHAR(3) NOT NULL,               -- FK to Country table
    FOREIGN KEY (CountryID) REFERENCES address.country(CountryID)
);

