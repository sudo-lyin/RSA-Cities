    CityID VARCHAR(16) PRIMARY KEY,                         -- e.g., 'GAU-JHB' for Johannesburg in Gauteng
    CityName NVARCHAR(100) NOT NULL,
    RegionID CHAR(7) NOT NULL,
    FOREIGN KEY (RegionID) REFERENCES address.region(RegionID),
    CONSTRAINT UQ_City_Per_Region UNIQUE (CityName, RegionID) -- Prevents duplicate city names per region
);

